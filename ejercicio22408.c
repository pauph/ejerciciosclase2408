#include <stdio.h>
#include <math.h>

int main(){
	
	int a = 5;
	int b = 6;
	float c = 4.6;
	float d = 9.2;
	
	int sumaValores = a+b;
	int restaValores = a-b;
	float multiplicacionValores = c*d;
	float divisionValores = d/c;
	
	
	printf("La suma es igual a: %d/n", sumaValores);
	printf("La suma es igual a: %d/n", restaValores);
	printf("La suma es igual a: %f/n", multiplicacionValores);
	printf("La suma es igual a: %f/n", divisionValores);
}
